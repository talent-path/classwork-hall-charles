package com.tp.LeagueApp.models;

public class Rune {

    Integer runeId;
    String runeName, runeDescription;

    public Integer getRuneId() {
        return runeId;
    }

    public void setRuneId(Integer runeId) {
        this.runeId = runeId;
    }

    public String getRuneName() {
        return runeName;
    }

    public void setRuneName(String runeName) {
        this.runeName = runeName;
    }

    public String getRuneDescription() {
        return runeDescription;
    }

    public void setRuneDescription(String runeDescription) {
        this.runeDescription = runeDescription;
    }
}
